Point3d CubeVtx::findSurface (CubeVtx& endPoint, 
						   Double threshold)
{
	Double alpha = (fabs(value) / 
					(fabs(value) + fabs(endPoint.value)));
	return ((this->pos) * (1 - alpha) + endPoint.pos * alpha);
	
}

